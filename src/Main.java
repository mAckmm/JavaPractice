/*
JVM ->
Loads, verifies and Executes code.
It does not exist physically and is platform independent.

JRE ->
JVM + libraries that JVM uses at runtime.
It physically exists and is platform dependent.

JDK ->
Full featured software development kit.
Contains JRE and development tools.
*/
